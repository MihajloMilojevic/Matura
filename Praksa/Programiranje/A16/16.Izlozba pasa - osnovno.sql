CREATE TABLE BOJA(
   id_boje  INT PRIMARY KEY,
   boja  NVARCHAR(30)  NOT NULL
);

CREATE TABLE DUZINA_DLAKE(
  id_duzine INT PRIMARY KEY,
  duzina REAL NOT NULL
);

CREATE TABLE RASA(
  id_rase INT PRIMARY KEY,
  rasa NVARCHAR(30)
);

CREATE TABLE POL(
   id_pola INT PRIMARY KEY,
   pol NVARCHAR(30) NOT NULL
);

CREATE TABLE KATEGORIJA(
   id_kategorije INT PRIMARY KEY,
   naziv NVARCHAR(30)
);

CREATE TABLE IZLOZBA(
   id_izlozbe NVARCHAR(30) PRIMARY KEY,
   mesto NVARCHAR(30) NOT NULL,
   datum DATE NOT NULL
);



CREATE TABLE VLASNIK(
  vlasnik_id INT PRIMARY KEY,
  ime NVARCHAR(30) NOT NULL,
  prezime NVARCHAR(30) NOT NULL,
  adresa NVARCHAR(30) NOT NULL,
  JMBG VARCHAR(13)
);



CREATE TABLE PAS(
    pas_id INT PRIMARY KEY,
    ime NVARCHAR(30) NOT NULL,
    tezina INT NOT NULL,
    id_duzine INT NOT NULL REFERENCES DUZINA_DLAKE(id_duzine),
    id_rase INT NOT NULL REFERENCES RASA(id_rase),
    id_boje INT NOT NULL REFERENCES BOJA(id_boje),
    id_pola INT NOT NULL REFERENCES POL(id_pola),
    id_vlasnika INT NOT NULL REFERENCES VLASNIK(vlasnik_id),
    vakcinisan VARCHAR(2)
);

CREATE TABLE REZULTAT(
   id_izlozbe NVARCHAR(30) REFERENCES IZLOZBA(id_izlozbe),
   id_kategorije INT REFERENCES KATEGORIJA(id_kategorije),
   pas_id INT REFERENCES PAS(pas_id),
   rezultat INT,
   napomena NVARCHAR(30),
   PRIMARY KEY(id_izlozbe, id_kategorije,pas_id)
);

